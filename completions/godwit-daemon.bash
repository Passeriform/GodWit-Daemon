_godwit-daemon() {
    local i cur prev opts cmds
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${i}" in
            godwit-daemon)
                cmd="godwit-daemon"
                ;;
            
            die)
                cmd+="__die"
                ;;
            help)
                cmd+="__help"
                ;;
            new)
                cmd+="__new"
                ;;
            prune)
                cmd+="__prune"
                ;;
            regress)
                cmd+="__regress"
                ;;
            split)
                cmd+="__split"
                ;;
            trace)
                cmd+="__trace"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        godwit-daemon)
            opts=" -q -v -h -V  --quiet --verbose --help --version   new regress die help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        
        godwit__daemon__die)
            opts=" -h -V -q -v  --help --version --quiet --verbose   trace split prune help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        godwit__daemon__die__help)
            opts=" -h -V -q -v  --help --version --quiet --verbose  "
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        godwit__daemon__die__prune)
            opts=" -h -V -q -v  --help --version --quiet --verbose  <application> "
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        godwit__daemon__die__split)
